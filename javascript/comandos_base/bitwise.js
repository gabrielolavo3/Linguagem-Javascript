/* Bitwise é a operação lógica com os bits da memória, manipulando valores em binário
 O resultado da operação é a conversão de um número binário para decimal */

valor1 = 10
valor2 = 12

console.log(valor1 & valor2)
console.log(valor1 | valor2)
console.log(valor1 << 1) // Desloca 1 bit para a esquerda, dobrando o valor do resultado
console.log(valor2 >> 2) // Desloca 2 bit para a direita, dividindo o valor do resultado