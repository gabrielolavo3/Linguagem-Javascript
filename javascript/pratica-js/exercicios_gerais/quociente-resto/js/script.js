"use strict"

let primeiroValor,
    segundoValor

primeiroValor = Number(prompt("Digite o 1º valor: "))
segundoValor = Number(prompt("Digite o 2º valor: "))

console.log (`O quociente entre ${primeiroValor} e ${segundoValor} é ${primeiroValor / segundoValor}`)
console.log (`O resto entre ${primeiroValor} e ${segundoValor} é ${primeiroValor % segundoValor}`)